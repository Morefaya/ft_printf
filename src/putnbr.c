#include "ft_printf.h"

void	putnbr(long nbr)
{
	if (nbr == 9223372036854775807)
	{
		ft_putstr("9223372036854775807");
		return ;
	}
	if (nbr < 0)
	{
		nbr *= -1;
		ft_putchar('-');
	}
	if (nbr > 9)
	{
		putnbr(nbr / 10);
		putnbr(nbr % 10);
	}
	else
		ft_putchar('0' + nbr);
}
